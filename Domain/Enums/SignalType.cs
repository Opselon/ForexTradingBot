namespace Domain.Enums
{
    /// <summary>
    /// انواع مختلف سیگنال‌های معاملاتی یا طبقه‌بندی آن‌ها را مشخص می‌کند.
    /// این enum می‌تواند هم جهت سیگنال (خرید/فروش) و هم نوع تحلیل یا منبع آن را پوشش دهد.
    /// نکته: در نظر بگیرید که آیا تفکیک جهت سیگنال از نوع تحلیل/منبع آن به عنوان enum های جداگانه
    /// می‌تواند منجر به طراحی واضح‌تری شود.
    /// </summary>
    public enum SignalType
    {
        /// <summary>
        /// نشان‌دهنده یک سیگنال برای ورود به موقعیت خرید.
        /// مقدار: 0
        /// </summary>
        Buy = 0,

        /// <summary>
        /// نشان‌دهنده یک سیگنال برای ورود به موقعیت فروش.
        /// مقدار: 1
        /// </summary>
        Sell = 1,

        /// <summary>
        /// نشان‌دهنده یک سیگنال یا تحلیل با دیدگاه خنثی یا بدون جهت مشخص.
        /// ممکن است برای اطلاع‌رسانی یا هشدارهایی که جهت خرید/فروش ندارند استفاده شود.
        /// مقدار: 2
        /// </summary>
        Neutral = 2,

        /// <summary>
        /// نشان‌دهنده سیگنالی که عمدتاً بر اساس اخبار و رویدادهای اخیر ایجاد شده است.
        /// این می‌تواند به عنوان یک طبقه‌بندی برای منبع یا محرک سیگنال عمل کند.
        /// مقدار: 3
        /// </summary>
        NewsBased = 3,

        /// <summary>
        /// نشان‌دهنده سیگنالی که عمدتاً بر اساس تحلیل تکنیکال چارت‌ها و اندیکاتورها ایجاد شده است.
        /// این می‌تواند به عنوان یک طبقه‌بندی برای روش تحلیل استفاده شود.
        /// مقدار: 4
        /// </summary>
        TechnicalAnalysis = 4,

        /// <summary>
        /// نشان‌دهنده سیگنالی که عمدتاً بر اساس تحلیل بنیادی عوامل اقتصادی و مالی ایجاد شده است.
        /// این می‌تواند به عنوان یک طبقه‌بندی برای روش تحلیل استفاده شود.
        /// مقدار: 5
        /// </summary>
        FundamentalAnalysis = 5
    }
}