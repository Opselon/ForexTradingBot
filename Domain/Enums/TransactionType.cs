using System;

namespace Domain.Enums
{
    /// <summary>
    /// انواع مختلف تراکنش‌های مالی یا توکنی را که می‌توانند در سیستم رخ دهند، مشخص می‌کند.
    /// هر عضو این enum یک نوع خاص از عملیات مالی یا تغییر در موجودی را نشان می‌دهد.
    /// </summary>
    public enum TransactionType
    {
        /// <summary>
        /// نشان‌دهنده واریز وجه یا توکن به حساب کاربر.
        /// این نوع تراکنش معمولاً موجودی کاربر را افزایش می‌دهد.
        /// مقدار: 0
        /// </summary>
        Deposit = 0,

        /// <summary>
        /// نشان‌دهنده برداشت وجه یا توکن از حساب کاربر.
        /// این نوع تراکنش معمولاً موجودی کاربر را کاهش می‌دهد.
        /// (توجه: پیاده‌سازی برداشت ممکن است نیازمند ملاحظات امنیتی و فرآیندی خاص باشد).
        /// مقدار: 1
        /// </summary>
        Withdraw = 1,

        /// <summary>
        /// نشان‌دهنده پرداختی که توسط کاربر برای خرید یا تمدید یک اشتراک انجام شده است.
        /// این نوع تراکنش معمولاً از موجودی کاربر کسر می‌شود یا مستقیماً پرداخت می‌شود.
        /// مقدار: 2
        /// </summary>
        SubscriptionPayment = 2,

        /// <summary>
        /// نشان‌دهنده خرید توکن‌های داخلی سیستم توسط کاربر.
        /// این نوع تراکنش معمولاً شامل پرداخت وجه واقعی و افزایش موجودی توکن کاربر است.
        /// مقدار: 3
        /// </summary>
        TokenPurchase = 3,

        // ملاحظه: می‌توان انواع دیگری مانند 'ServiceFee', 'Refund', 'BonusToken' و غیره را در آینده اضافه کرد.

        /// <summary>
        /// نشان‌دهنده یک تعدیل در موجودی کاربر که توسط مدیر سیستم انجام شده است.
        /// این می‌تواند برای اصلاح خطاها، اعطای پاداش دستی یا سایر موارد مدیریتی استفاده شود.
        /// استفاده از یک مقدار عددی بالاتر (مانند 99) می‌تواند به تمایز آن از تراکنش‌های استاندارد کاربر کمک کند.
        /// مقدار: 99
        /// </summary>
        AdminAdjustment = 99
    }
}