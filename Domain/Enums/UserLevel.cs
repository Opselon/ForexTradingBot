namespace Domain.Enums
{
    /// <summary>
    /// سطوح مختلف دسترسی یا عضویت کاربران در سیستم را مشخص می‌کند.
    /// هر سطح می‌تواند با مجموعه‌ای از امکانات، محدودیت‌ها یا مزایای خاص همراه باشد.
    /// </summary>
    public enum UserLevel
    {
        /// <summary>
        /// سطح کاربری رایگان.
        /// کاربران با این سطح معمولاً به امکانات پایه و محدودی دسترسی دارند.
        /// مقدار: 0
        /// </summary>
        Free = 0,

        /// <summary>
        /// سطح عضویت برنزی.
        /// اولین سطح پولی یا سطح بالاتر از رایگان، با دسترسی به امکانات بیشتر نسبت به سطح Free.
        /// مقدار: 1
        /// </summary>
        Bronze = 1,

        /// <summary>
        /// سطح عضویت نقره‌ای.
        /// سطح متوسط عضویت با امکانات و مزایای بیشتر نسبت به سطح Bronze.
        /// مقدار: 2
        /// </summary>
        Silver = 2,

        /// <summary>
        /// سطح عضویت طلایی.
        /// سطح بالای عضویت با دسترسی گسترده به امکانات و احتمالاً پشتیبانی ویژه.
        /// مقدار: 3
        /// </summary>
        Gold = 3,

        /// <summary>
        /// بالاترین سطح عضویت کاربران عادی (پلاتینیوم).
        /// ارائه دهنده بیشترین امکانات، خدمات اختصاصی و مزایای ویژه.
        /// مقدار: 4
        /// </summary>
        Platinum = 4,

        // ملاحظه: می‌توان سطوح دیگری مانند 'Trial' (برای دوره آزمایشی) یا سطوح ویژه دیگری اضافه کرد.
        // همچنین ترتیب عددی سطوح می‌تواند در منطق برنامه برای بررسی "بالاتر بودن" یک سطح نسبت به دیگری استفاده شود.

        /// <summary>
        /// سطح دسترwadسی مدیر سیستم.
        /// کاربران با این سطح دارای اختیارات کامل برای مدیریت سیستم، کاربران و محتوا هستند.
        /// استفاده از یک مقدار عددی بالاتر (مانند 100) به تمایز آن از سطوح استاندارد کاربر کمک می‌کند.
        /// مقدار: 100
        /// </summary>
        Admin = 100
    }
}