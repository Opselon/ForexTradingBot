# --- Optional: Clean up old/unused Docker images ---
# log_info "Cleaning up unused Docker images (dangling and older versions)..."
# docker image prune -af || log_warn "Failed to prune Docker images. This is non-critical."

log_header "Deployment Script Finished Successfully"
exit 0