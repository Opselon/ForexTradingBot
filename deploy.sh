#   if docker inspect "$WEBAPI_CONTAINER_NAME" --format '{{.State.Health.Status}}' | grep -q "healthy"; then
#     log_info "WebAPI service is healthy."
#   elif docker inspect "$WEBAPI_CONTAINER_NAME" --format '{{.State.Status}}' | grep -q "running"; then
#     log_warn "WebAPI service is running but health status is not 'healthy' or not available. Check service logs."
#     docker compose --env-file "$ENV_FILE_NAME" -f "$DOCKER_COMPOSE_FILE_NAME" logs webapi --tail 50
#   else
#     log_error "WebAPI service is not running. Deployment may have failed."
#   fi
# else
#   log_error "WebAPI container not found. Deployment likely failed."
# fi

# --- Clean up old Docker images (optional) ---
# log_info "Cleaning up dangling Docker images..."
# docker image prune -af || log_warn "Failed to prune Docker images."

log_info "--- Deployment Script Finished Successfully ---"